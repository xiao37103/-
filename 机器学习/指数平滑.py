import pandas as pd
from statsmodels.tsa.holtwinters import ExponentialSmoothing

# 可以使用时间序列分析方法对每个市的历史数据进行分析，然后利用所得模型进行预测。


data = {'地市': ['合肥', '芜湖', '蚌埠', '淮南', '马鞍山',
                 '淮北', '铜陵', '安庆', '黄山', '阜阳',
                 '宿州', '滁州', '六安', '宜城', '池州', '亳州', '全省'],
        '2018年': [15634, 7869, 1969, 1322, 1152,
                   902, 736, 1534, 594, 5218,
                   3383, 2397, 2910, 1289, 459, 3140, 50508],
        '2019年': [26085, 9313, 2482, 1650, 1767,
                   1252, 1219, 2473, 847, 7295,
                   4287, 3610, 3671, 1911, 685, 4910, 73457],
        '2020年': [35412, 11281, 3290, 2158, 2953,
                   1723, 1722, 3584, 1172, 9999,
                   5855, 4779, 4918, 2665, 1032, 6470, 99013],
        '2021年': [61760, 16527, 5674, 4691, 6585,
                   3879, 3556, 7604, 2327, 18882,
                   10295, 8671, 8966, 5828, 2111, 11945, 179301],
        '2022年': [130823, 29674, 11281, 10742, 14360,
                   7587, 7132, 15791, 5108, 37499,
                   18974, 16913, 17070, 12907, 4893, 27618, 368372],
        '2023年': [141275, 31559, 12169, 11394, 15391,
                   8115, 7670, 17025, 5542, 39902,
                   20217, 18027, 18247, 13820, 5394, 29284, 395031]}

for city in data['地市']:
    # 转换为时间序列格式
    ts_data = pd.Series(data[city][1:], index=pd.date_range(start='2019-01-01', periods=6, freq='Y'))
    # 使用Exponential Smoothing方法拟合时间序列，并预测未来6年的数据
    model = ExponentialSmoothing(ts_data)
    fit_model = model.fit()
    forecast = fit_model.forecast(steps=8)
    # 将预测结果添加到原始数据字典中
    data[city].extend(forecast)
# 打印预测结果
print(pd.DataFrame(data))
